import { toast, Bounce } from "react-toastify";

export const commonFunctions = {
  success,
  failed,
  warning,
};

function warning(message) {
  return toast.warn(message, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    transition: Bounce,
  });
}
